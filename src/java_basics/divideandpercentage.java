package java_basics;


public class divideandpercentage {
    public static void main(String[] args) {


        int bigInt = 20;
        int smallInt = 5;
        int intResult = bigInt / smallInt;
        System.out.println(intResult);


        double bigDouble = 20.0;
        int smallDouble = 5;
        double doubleResult = bigDouble / smallDouble;
        System.out.println(doubleResult);
    }
}






